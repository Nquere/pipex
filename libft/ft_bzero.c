#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	int		a;

	a = 0;
	i = 0;
	while (i < n)
	{
		((char*)s)[a] = 0;
		a = a + 1;
		i = i + 1;
	}
}
