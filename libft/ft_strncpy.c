#include <string.h>

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t	a;

	a = 0;
	while (s2[a] != '\0' && a < n)
	{
		s1[a] = s2[a];
		a = a + 1;
	}
	if (s2[a] == '\0')
	{
		while (a < n)
		{
			s1[a] = '\0';
			a = a + 1;
		}
	}
	return (s1);
}
