#include "libft.h"
#include <stdlib.h>

void	ft_putstr(char const *s)
{
	int		a;

	a = 0;
	if (s == NULL)
		return ;
	while (s[a] != '\0')
	{
		ft_putchar(s[a]);
		a = a + 1;
	}
}
