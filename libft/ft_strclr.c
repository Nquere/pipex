#include <string.h>
#include "libft.h"

void	ft_strclr(char *s)
{
	int		a;

	a = 0;
	if (s == NULL)
		return ;
	while (s[a] != '\0')
	{
		s[a] = '\0';
		a = a + 1;
	}
}
