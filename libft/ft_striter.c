#include <stdlib.h>
#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		i;

	i = 0;
	if (s == NULL)
		return ;
	while (s[i] != '\0')
	{
		f(&s[i]);
		i = i + 1;
	}
}
